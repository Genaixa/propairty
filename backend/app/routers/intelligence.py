    """Find leases where rent hasn't been reviewed in 12+ months."""
    from app.models.renewal import LeaseRenewal
    today = date.today()
    cutoff = today.replace(year=today.year - 1)

    leases = (
        db.query(Lease).join(Unit).join(Property)
        .filter(Property.organisation_id == current_user.organisation_id, Lease.status == "active")
        .all()
    )

    results = []
    for lease in leases:
        last_review = (
            db.query(LeaseRenewal)
            .filter(LeaseRenewal.lease_id == lease.id, LeaseRenewal.status == "accepted")
            .order_by(LeaseRenewal.sent_at.desc())
            .first()
        )
        review_date = last_review.sent_at.date() if last_review else lease.start_date
        months_since = (today.year - review_date.year) * 12 + today.month - review_date.month

        if months_since >= 12:
            unit = lease.unit
            prop = unit.property if unit else None
            tenant = lease.tenant
            results.append({
                "lease_id": lease.id,
                "tenant_name": tenant.full_name if tenant else "Unknown",
                "property_name": prop.name if prop else "",
                "unit_name": unit.name if unit else "",
                "postcode": prop.postcode if prop else "",
                "bedrooms": unit.bedrooms if unit else 0,
                "current_rent": lease.monthly_rent,
                "months_since_review": months_since,
                "last_review_date": review_date.isoformat(),
            })

    results.sort(key=lambda x: x["months_since_review"], reverse=True)
    return results


