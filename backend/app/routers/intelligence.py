def _generate_claim_pdf(prop, unit, req, data: dict, org, photos_count: int, invoices_count: int) -> str | None:
    try:
        from fpdf import FPDF

        pdf = FPDF()
        pdf.add_page()
        pdf.set_auto_page_break(auto=True, margin=15)

        # Header
        pdf.set_font("Helvetica", "B", 18)
        pdf.set_text_color(79, 70, 229)
        pdf.cell(0, 10, "PropAIrty", ln=True)
        pdf.set_text_color(0, 0, 0)
        pdf.set_font("Helvetica", "B", 14)
        pdf.cell(0, 8, "Insurance Claim Document", ln=True)
        pdf.set_font("Helvetica", "", 9)
        pdf.set_text_color(130, 130, 130)
        pdf.cell(0, 5, f"Generated: {datetime.now().strftime('%d %B %Y')}", ln=True)
        pdf.ln(2)
        pdf.set_draw_color(230, 230, 230)
        pdf.line(10, pdf.get_y(), 200, pdf.get_y())
        pdf.ln(5)
        pdf.set_text_color(0, 0, 0)

        def section(title):
            pdf.set_font("Helvetica", "B", 11)
            pdf.set_fill_color(247, 247, 255)
            pdf.cell(0, 7, f"  {title}", ln=True, fill=True)
            pdf.ln(2)
            pdf.set_font("Helvetica", "", 10)

        def row(label, value):
            pdf.set_font("Helvetica", "B", 10)
            pdf.cell(50, 6, label)
            pdf.set_font("Helvetica", "", 10)
            pdf.multi_cell(0, 6, str(value))

        # Claim details
        section("Claim Details")
        row("Reference:", data.get("claim_reference", "N/A"))
        row("Agency:", org.name if org else "N/A")
        row("Property:", f"{prop.name}, {prop.address_line1}, {prop.city}, {prop.postcode}")
        if unit:
            row("Unit:", unit.name)
        row("Incident Date:", req.incident_date)
        row("Claim Type:", req.claim_type.replace("_", " ").title())
        pdf.ln(3)

        # Summary
        if data.get("summary"):
            section("Claim Summary")
            pdf.multi_cell(0, 5, data["summary"])
            pdf.ln(3)

        # Damage description
        if data.get("damage_description"):
            section("Description of Damage / Loss")
            pdf.multi_cell(0, 5, data["damage_description"])
            pdf.ln(3)

        # Timeline
        if data.get("timeline"):
            section("Timeline of Events")
            for item in data["timeline"]:
                pdf.cell(5)
                pdf.multi_cell(0, 5, f"• {item}")
            pdf.ln(3)

        # Estimated value
        if data.get("estimated_claim_min"):
            section("Estimated Claim Value")
            pdf.set_font("Helvetica", "B", 12)
            pdf.set_text_color(79, 70, 229)
            pdf.cell(0, 7, f"£{data['estimated_claim_min']:,} — £{data['estimated_claim_max']:,}", ln=True)
            pdf.set_text_color(0, 0, 0)
            pdf.ln(2)

        # Evidence
        section("Evidence Available")
        pdf.set_font("Helvetica", "", 10)
        pdf.cell(0, 5, f"• {photos_count} property photo(s) on file in PropAIrty", ln=True)
        pdf.cell(0, 5, f"• {invoices_count} invoice(s) on file in PropAIrty", ln=True)
        for ev in data.get("evidence_available", []):
            pdf.cell(0, 5, f"• {ev}", ln=True)
        pdf.ln(3)

        # Checklist
        if data.get("supporting_documents_checklist"):
            section("Supporting Documents Checklist")
            for item in data["supporting_documents_checklist"]:
                pdf.cell(0, 5, f"☐  {item}", ln=True)
            pdf.ln(3)

        # Next steps
        if data.get("next_steps"):
            section("Next Steps")
            for i, step in enumerate(data["next_steps"], 1):
                pdf.multi_cell(0, 5, f"{i}. {step}")
            pdf.ln(3)

        # Footer
        pdf.set_font("Helvetica", "", 8)
        pdf.set_text_color(150, 150, 150)
        pdf.cell(0, 5, "Generated by PropAIrty · propairty.co.uk · This document is for reference only.", ln=True)

        filename = f"claim_{prop.id}_{int(time.time())}.pdf"
        pdf.output(str(PDF_DIR / filename))
        return filename
    except Exception:
        return None


