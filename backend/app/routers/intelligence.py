@router.get("/health-score")
def health_score(
    db: Session = Depends(get_db),
    current_user: User = Depends(get_current_user),
):
    """0-100 score across arrears, compliance, maintenance, voids, renewals."""
    org_id = current_user.organisation_id
    now = datetime.now(timezone.utc)
    today = date.today()

    # --- Arrears (25pts) ---
    total_leases = db.query(Lease).join(Unit).join(Property).filter(
        Property.organisation_id == org_id, Lease.status == "active"
    ).count()
    overdue = db.query(RentPayment).filter(
        RentPayment.lease_id.in_(db.query(Lease.id).join(Unit).join(Property).filter(
            Property.organisation_id == org_id
        )),
        RentPayment.status == "overdue",
    ).count()
    arrears_score = 25 if total_leases == 0 else max(0, 25 - int((overdue / max(total_leases, 1)) * 25 * 4))

    # --- Compliance (25pts) ---
    total_certs = db.query(ComplianceCertificate).join(Property).filter(
        Property.organisation_id == org_id
    ).count()
    expired = db.query(ComplianceCertificate).join(Property).filter(
        Property.organisation_id == org_id,
        ComplianceCertificate.expiry_date < today,
    ).count()
    expiring_soon = db.query(ComplianceCertificate).join(Property).filter(
        Property.organisation_id == org_id,
        ComplianceCertificate.expiry_date >= today,
        ComplianceCertificate.expiry_date <= today + timedelta(days=30),
    ).count()
    compliance_score = 25 if total_certs == 0 else max(0, 25 - expired * 5 - expiring_soon * 2)
    compliance_score = min(25, compliance_score)

    # --- Maintenance (20pts) ---
    open_jobs = db.query(MaintenanceRequest).filter(
        MaintenanceRequest.organisation_id == org_id,
        MaintenanceRequest.status.in_(["open", "in_progress"]),
    ).count()
    high_priority_open = db.query(MaintenanceRequest).filter(
        MaintenanceRequest.organisation_id == org_id,
        MaintenanceRequest.status.in_(["open", "in_progress"]),
        MaintenanceRequest.priority == "high",
    ).count()
    maintenance_score = max(0, 20 - high_priority_open * 4 - max(0, open_jobs - 2) * 1)
    maintenance_score = min(20, maintenance_score)

    # --- Voids (15pts) ---
    total_units = db.query(Unit).join(Property).filter(
        Property.organisation_id == org_id
    ).count()
    vacant_units = db.query(Unit).join(Property).filter(
        Property.organisation_id == org_id,
        Unit.status == "vacant",
    ).count()
    void_rate = vacant_units / max(total_units, 1)
    void_score = max(0, int(15 - void_rate * 50))
    void_score = min(15, void_score)

    # --- Renewals (15pts) ---
    expiring_leases = db.query(Lease).join(Unit).join(Property).filter(
        Property.organisation_id == org_id,
        Lease.status == "active",
        Lease.end_date <= today + timedelta(days=60),
        Lease.end_date >= today,
    ).count()
    renewal_score = max(0, 15 - expiring_leases * 3)
    renewal_score = min(15, renewal_score)

    total = arrears_score + compliance_score + maintenance_score + void_score + renewal_score

    issues = []
    if expired > 0:
        issues.append(f"{expired} expired compliance certificate{'s' if expired > 1 else ''}")
    if expiring_soon > 0:
        issues.append(f"{expiring_soon} compliance cert{'s' if expiring_soon > 1 else ''} expiring within 30 days")
    if overdue > 0:
        issues.append(f"{overdue} overdue rent payment{'s' if overdue > 1 else ''}")
    if high_priority_open > 0:
        issues.append(f"{high_priority_open} high-priority maintenance job{'s' if high_priority_open > 1 else ''} open")
    if vacant_units > 0:
        issues.append(f"{vacant_units} vacant unit{'s' if vacant_units > 1 else ''}")
    if expiring_leases > 0:
        issues.append(f"{expiring_leases} lease{'s' if expiring_leases > 1 else ''} expiring within 60 days")

    return {
        "score": total,
        "breakdown": {
            "arrears": arrears_score,
            "compliance": compliance_score,
            "maintenance": maintenance_score,
            "voids": void_score,
            "renewals": renewal_score,
        },
        "max": {"arrears": 25, "compliance": 25, "maintenance": 20, "voids": 15, "renewals": 15},
        "issues": issues,
        "grade": "Excellent" if total >= 90 else "Good" if total >= 70 else "Needs Attention" if total >= 50 else "Critical",
    }


