@router.get("/epc-procurement")
def epc_procurement(
    db: Session = Depends(get_db),
    current_user: User = Depends(get_current_user),
):
    """Group all EPC improvements across the portfolio by improvement type."""
    import json as _json
    from app.models.epc_roadmap import PropertyEpcRoadmap

    roadmaps = db.query(PropertyEpcRoadmap).filter_by(
        organisation_id=current_user.organisation_id
    ).all()

    # Group improvements by normalised title
    groups = {}
    for rm in roadmaps:
        try:
            improvements = _json.loads(rm.improvements_json)
        except Exception:
            continue
        for imp in improvements:
            title = imp.get("title", "Unknown")
            if title not in groups:
                groups[title] = {
                    "title": title,
                    "trade": imp.get("trade", "general"),
                    "properties": [],
                    "total_cost_min": 0,
                    "total_cost_max": 0,
                    "earliest_deadline": None,
                    "latest_start_dates": [],
                }
            g = groups[title]
            g["properties"].append({
                "property_id": rm.property_id,
                "property_name": f"{rm.property_name} — {rm.unit_name}" if rm.unit_name else rm.property_name,
                "epc_rating": rm.epc_rating,
                "target_date": rm.target_date,
                "cost_min": imp.get("estimated_cost_min"),
                "cost_max": imp.get("estimated_cost_max"),
                "latest_start_date": imp.get("latest_start_date"),
                "days_until_start": imp.get("days_until_start"),
                "duration_days": imp.get("duration_days"),
                "epc_points": imp.get("epc_points"),
            })
            g["total_cost_min"] += imp.get("estimated_cost_min") or 0
            g["total_cost_max"] += imp.get("estimated_cost_max") or 0
            if rm.target_date:
                if not g["earliest_deadline"] or rm.target_date < g["earliest_deadline"]:
                    g["earliest_deadline"] = rm.target_date
            if imp.get("latest_start_date"):
                g["latest_start_dates"].append(imp["latest_start_date"])

    result = list(groups.values())
    # Sort by earliest latest_start_date (most urgent first)
    def sort_key(g):
        dates = g["latest_start_dates"]
        return min(dates) if dates else "9999"
    result.sort(key=sort_key)
    return result


