@router.get("/void-risk")
def void_risk(
    db: Session = Depends(get_db),
    current_user: User = Depends(get_current_user),
):
    """Identify leases likely to result in a void: expiring with no active renewal."""
    org_id = current_user.organisation_id
    today = date.today()
    horizon = today + timedelta(days=180)

    leases = (
        db.query(Lease)
        .join(Unit).join(Property)
        .filter(
            Property.organisation_id == org_id,
            Lease.status == "active",
            Lease.end_date != None,
            Lease.end_date <= horizon,
        )
        .all()
    )

    results = []
    for lease in leases:
        # Check renewal state
        latest_renewal = (
            db.query(LeaseRenewal)
            .filter(LeaseRenewal.lease_id == lease.id)
            .order_by(LeaseRenewal.sent_at.desc())
            .first()
        )
        renewal_status = latest_renewal.status if latest_renewal else None

        # Skip leases that have an accepted renewal
        if renewal_status == "accepted":
            continue

        days_left = (lease.end_date - today).days
        if days_left <= 30:
            risk = "30"
        elif days_left <= 60:
            risk = "60"
        elif days_left <= 90:
            risk = "90"
        elif days_left <= 120:
            risk = "120"
        else:
            risk = "120+"

        unit = lease.unit
        prop = unit.property if unit else None
        tenant = lease.tenant

        results.append({
            "lease_id": lease.id,
            "tenant_name": tenant.full_name if tenant else "Unknown",
            "tenant_email": tenant.email if tenant else None,
            "property_name": prop.name if prop else "",
            "unit_name": unit.name if unit else "",
            "postcode": prop.postcode if prop else "",
            "bedrooms": unit.bedrooms if unit else 0,
            "monthly_rent": lease.monthly_rent,
            "lease_end": lease.end_date.isoformat(),
            "days_left": days_left,
            "renewal_status": renewal_status,
            "risk": risk,
            "property_id": prop.id if prop else None,
            "unit_id": unit.id if unit else None,
        })

    results.sort(key=lambda x: x["days_left"])
    return results


