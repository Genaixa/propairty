@router.get("/tax-summary/{landlord_id}")
def tax_summary(
    landlord_id: int,
    tax_year: int = None,
    db: Session = Depends(get_db),
    current_user: User = Depends(get_current_user),
):
    """Annual rental income and allowable expenses summary for a landlord."""
    from app.models.landlord import Landlord
    landlord = db.query(Landlord).filter(
        Landlord.id == landlord_id,
        Landlord.organisation_id == current_user.organisation_id,
    ).first()
    if not landlord:
        raise HTTPException(status_code=404, detail="Landlord not found")

    if not tax_year:
        today = date.today()
        tax_year = today.year if today.month >= 4 else today.year - 1

    # UK tax year: 6 April to 5 April
    year_start = date(tax_year, 4, 6)
    year_end = date(tax_year + 1, 4, 5)

    # Properties belonging to this landlord
    props = db.query(Property).filter(
        Property.organisation_id == current_user.organisation_id,
        Property.landlord_id == landlord_id,
    ).all()
    prop_ids = [p.id for p in props]

    if not prop_ids:
        return {"landlord": landlord.full_name, "tax_year": f"{tax_year}/{str(tax_year+1)[-2:]}", "properties": [], "totals": {}}

    # Build unit lookup for all properties (used for maintenance queries, consistent with Accounting page)
    all_unit_ids = [u.id for p in props for u in p.units]

    # Rent income
    lease_ids = db.query(Lease.id).join(Unit).filter(Unit.property_id.in_(prop_ids)).subquery()
    payments = db.query(RentPayment).filter(
        RentPayment.lease_id.in_(lease_ids),
        RentPayment.paid_date >= year_start,
        RentPayment.paid_date <= year_end,
        RentPayment.status == "paid",
    ).all()
    total_income = sum(p.amount_paid or p.amount_due for p in payments)

    # Maintenance costs — filter by unit_id (same as Accounting page) and date range
    maint_start = datetime.combine(year_start, datetime.min.time())
    maint_end = datetime.combine(year_end, datetime.max.time())
    maint_jobs = db.query(MaintenanceRequest).filter(
        MaintenanceRequest.unit_id.in_(all_unit_ids),
        MaintenanceRequest.actual_cost.isnot(None),
        MaintenanceRequest.actual_cost > 0,
        MaintenanceRequest.created_at >= maint_start,
        MaintenanceRequest.created_at <= maint_end,
    ).all()
    total_maintenance = sum(j.actual_cost for j in maint_jobs if j.actual_cost)

    # Management fees — use landlord's agreed rate, fall back to 10% if not set
    fee_is_estimate = not landlord.management_fee_pct
    fee_rate = (landlord.management_fee_pct / 100.0) if landlord.management_fee_pct else 0.10
    mgmt_fee_est = round(total_income * fee_rate, 2)

    # Per property breakdown
    property_breakdown = []
    for prop in props:
        prop_unit_ids = [u.id for u in prop.units]
        prop_lease_ids = db.query(Lease.id).join(Unit).filter(Unit.property_id == prop.id).subquery()
        prop_payments = db.query(RentPayment).filter(
            RentPayment.lease_id.in_(prop_lease_ids),
            RentPayment.paid_date >= year_start,
            RentPayment.paid_date <= year_end,
            RentPayment.status == "paid",
        ).all()
        prop_income = sum(p.amount_paid or p.amount_due for p in prop_payments)

        prop_maint_jobs = db.query(MaintenanceRequest).filter(
            MaintenanceRequest.unit_id.in_(prop_unit_ids),
            MaintenanceRequest.actual_cost.isnot(None),
            MaintenanceRequest.actual_cost > 0,
            MaintenanceRequest.created_at >= maint_start,
            MaintenanceRequest.created_at <= maint_end,
        ).all()
        prop_maint_cost = sum(j.actual_cost for j in prop_maint_jobs if j.actual_cost)

        property_breakdown.append({
            "property_id": prop.id,
            "property_name": prop.name,
            "address": f"{prop.address_line1}, {prop.postcode}",
            "gross_income": round(prop_income, 2),
            "maintenance_costs": round(prop_maint_cost, 2),
        })

    net_profit_estimate = round(total_income - total_maintenance - mgmt_fee_est, 2)

    pdf_filename = _generate_tax_pdf(landlord, tax_year, year_start, year_end, total_income,
                                      total_maintenance, mgmt_fee_est, net_profit_estimate, property_breakdown)

    return {
        "landlord_name": landlord.full_name,
        "landlord_email": landlord.email,
        "tax_year": f"{tax_year}/{str(tax_year+1)[-2:]}",
        "period": f"6 April {tax_year} — 5 April {tax_year+1}",
        "properties_count": len(props),
        "gross_rental_income": round(total_income, 2),
        "maintenance_expenses": round(total_maintenance, 2),
        "management_fee_amount": mgmt_fee_est,
        "management_fee_estimate": mgmt_fee_est,  # kept for backwards compat
        "management_fee_rate_pct": round(fee_rate * 100, 2),
        "management_fee_is_estimate": fee_is_estimate,
        "net_profit_estimate": net_profit_estimate,
        "property_breakdown": property_breakdown,
        "pdf_url": f"/api/intelligence/tax-pdf/{pdf_filename}" if pdf_filename else None,
        "disclaimer": "This summary is for reference only and does not constitute tax advice. Please share with your accountant.",
    }


def _generate_tax_pdf(landlord, tax_year, year_start, year_end, income, maint, mgmt_fee, net, props) -> str | None:
    try:
        from fpdf import FPDF
        pdf = FPDF()
        pdf.add_page()
        pdf.set_auto_page_break(auto=True, margin=15)

        pdf.set_font("Helvetica", "B", 18)
        pdf.set_text_color(79, 70, 229)
        pdf.cell(0, 10, "PropAIrty", ln=True)
        pdf.set_text_color(0, 0, 0)
        pdf.set_font("Helvetica", "B", 14)
        pdf.cell(0, 8, f"Annual Rental Income Summary", ln=True)
        pdf.set_font("Helvetica", "", 10)
        pdf.set_text_color(100, 100, 100)
        pdf.cell(0, 6, f"Tax Year {tax_year}/{str(tax_year+1)[-2:]}  ·  {landlord.full_name}", ln=True)
        pdf.ln(3)
        pdf.set_draw_color(230, 230, 230)
        pdf.line(10, pdf.get_y(), 200, pdf.get_y())
        pdf.ln(6)
        pdf.set_text_color(0, 0, 0)

        def money_row(label, amount, bold=False, color=None):
            pdf.set_font("Helvetica", "B" if bold else "", 10)
            if color:
                pdf.set_text_color(*color)
            pdf.cell(130, 7, label)
            pdf.cell(0, 7, f"£{amount:>10,.2f}", ln=True)
            if color:
                pdf.set_text_color(0, 0, 0)

        pdf.set_font("Helvetica", "B", 11)
        pdf.cell(0, 7, "Income & Expenses", ln=True)
        pdf.ln(2)
        money_row("Gross rental income received", income)
        pdf.set_draw_color(200, 200, 200)
        pdf.line(10, pdf.get_y(), 200, pdf.get_y())
        pdf.ln(2)
        money_row("Maintenance & repairs", -maint)
        mgmt_pct = round(mgmt_fee / income * 100, 1) if income else 0
        money_row(f"Management fees ({mgmt_pct}% of gross income)", -mgmt_fee)
        pdf.set_draw_color(79, 70, 229)
        pdf.line(10, pdf.get_y(), 200, pdf.get_y())
        pdf.ln(2)
        money_row("Estimated net profit", net, bold=True, color=(79, 70, 229))
        pdf.ln(6)

        if props:
            pdf.set_font("Helvetica", "B", 11)
            pdf.cell(0, 7, "Property Breakdown", ln=True)
            pdf.ln(2)
            pdf.set_font("Helvetica", "B", 9)
            pdf.set_fill_color(247, 247, 255)
            pdf.cell(90, 6, "Property", fill=True)
            pdf.cell(40, 6, "Income", align="R", fill=True)
            pdf.cell(0, 6, "Maint. cost", align="R", fill=True, ln=True)
            pdf.set_font("Helvetica", "", 9)
            for p in props:
                pdf.cell(90, 6, p["property_name"][:40])
                pdf.cell(40, 6, f"£{p['gross_income']:,.0f}", align="R")
                pdf.cell(0, 6, f"£{p['maintenance_costs']:,.0f}", align="R", ln=True)
            pdf.ln(4)

        pdf.set_font("Helvetica", "I", 8)
        pdf.set_text_color(150, 150, 150)
        pdf.multi_cell(0, 4, "DISCLAIMER: This summary is for reference only and does not constitute tax advice. Actual allowable expenses may differ. Please share with a qualified accountant or tax adviser before filing your Self Assessment return.")

        filename = f"tax_{landlord.id}_{tax_year}_{int(time.time())}.pdf"
        pdf.output(str(PDF_DIR / filename))
        return filename
    except Exception:
        return None


